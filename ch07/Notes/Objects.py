#Complex objects - consist of 1 or more primitive or complex objects
#For example, a string is both a single object and a set of individual strings consisting of a single character
#The goal is to focus on the data rather than the algorithm
#example: Turtle: made up of primitive and complex objects
#primitive: built-ins(str, int, float, list, bool)
#Object Oriented programming is when you focus on objects and their behavior rather than functions
#In Python, all values are objects
#Objects have
#1. State: the current value of an object's data
#For example, Turtle objects have position, color, heading. This is the turtle's state
#2. Behavior: What an object can do, actions 
#For example: Turtle objects can go forward, backward, turn, etc. 
